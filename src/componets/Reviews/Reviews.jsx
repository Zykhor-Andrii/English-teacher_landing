export const Reviews = () => {
  const [isFullText, setIsFullText] = useState(null);
  const [windowWidth, setWindowWidth] = useState(window.innerWidth);


  const openText = (id) => {
    if (isFullText === id) {
      setIsFullText(null);
      return
    }

    setIsFullText(id);
  }

  useEffect(() => {
    const handleResize = () => {
      setWindowWidth(window.innerWidth);
    };

    window.addEventListener('resize', handleResize);

    return () => {
      window.removeEventListener('resize', handleResize);
    };
  }, []);

  useEffect(() => {
    if (windowWidth >= 768) {
      setIsFullText(null)
    }
  }, [windowWidth])
  return (
    <section id="reviews" className="page__section reviews">
      <div className="container">
        <h2 className="page__title">
          Відгуки
        </h2>
        <div className="reviews__content">
          {
            windowWidth < 768 && (
              mobileReviews.map(reviewers => (
                <div key={reviewers.id} className="reviews__item ">
                  <div className="reviews__info">
                    <img
                      src={reviewers.photo}
                      alt={reviewers.name}
                      className="reviews__photo"
                    />
                    <p className="reviews__name">
                      {reviewers.name}
                    </p>
                  </div>
                  <p
                    className="reviews__text"
                    onClick={() => openText(reviewers.id)}
                  >
                    {

                      isFullText === reviewers.id ? (
                        reviewers.fullText
                      ) : (
                        reviewers.shortText
                      )
                    }
                  </p>
                </div>
              ))
            )
          }
          {
            windowWidth > 768 && (
              tableReviews.map(reviewers => (
                <div key={reviewers.id} className="reviews__item ">
                  <div className="reviews__info">
                    <img
                      src={reviewers.photo}
                      alt={reviewers.name}
                      className="reviews__photo"
                    />
                    <p className="reviews__name">
                      {reviewers.name}
                    </p>
                  </div>
                  <p
                    className="reviews__text"
                  >
                    {
                      reviewers.text
                    }
                  </p>
                </div>
              ))
            )

          }

        </div>
      </div>
    </section>
  )
}
